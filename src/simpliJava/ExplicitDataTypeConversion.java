package simpliJava;

public class ExplicitDataTypeConversion {
	
	public static void main(String[] args) {
		double a = 50.50;
		System.out.println(a);
		
		float b = (float)a;
		System.out.println(b);
		
		long c = (long)a;
		System.out.println(c);
		
		int d = (int)c;
		System.out.println(d);
	}

}
